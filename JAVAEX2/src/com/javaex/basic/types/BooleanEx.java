package com.javaex.basic.types;

public class BooleanEx {

	public static void main(String[] args) {
		// True , False  조건문 제어문 흐름제어
		boolean b1 = true;
		boolean b2 = false;
		System.out.println(b1);
		
		// 비교 , 논리 연산의 결과로 추출된다
		int v1 = 3, v2 = 5;
		boolean result = v1 > v2;
		System.out.println(result);

	}

}
